/*

string -> Texts, letters ect.
number -> Numbers (Integers, floats)
boolean -> True/False

*/

//* Strings

let fName = "Ahmet";
let address = "Lincoln St. No 345";

console.log(fName);
console.log(address);

//* Numbers

let birthYear = 2000;
let today = "2023"; // Implicit Type Conversion  String -> Number

let age = today - birthYear;
console.log(age);

//* Boolean

let isOnline = true;
console.log(!isOnline);

// typeof metodu ile bir değişkenin türü belirlenebilir.

console.log(typeof age);